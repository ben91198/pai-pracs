; ALP FOR STRING CONCATANATION

.MODEL SMALL

XYZ MACRO NUM
     MOV AH, NUM
     INT 21H
ENDM

EXTRN MAX1 : BYTE
EXTRN ACTUAL1 : BYTE
EXTRN STRING1 : BYTE
EXTRN MAX2 : BYTE
EXTRN ACTUAL2 : BYTE
EXTRN STRING2 : BYTE
EXTRN M4 : BYTE

PUBLIC CONCATE

.DATA

CONC  DB 80 DUP(0)
SIZE1 DW 0

.CODE

CONCATE PROC FAR

 	MOV AX, @DATA
	MOV DS, AX
        	MOV ES, AX

	LEA SI, STRING1
        	LEA DI, CONC
	MOV CL, ACTUAL1
        	MOV CH, 0
        	MOV SIZE1,CX
	CLD
      REP    MOVSB

	LEA SI, STRING2
       	LEA DI, CONC
       	ADD DI, SIZE1
        	MOV CL, ACTUAL2
        	MOV CH, 0
         REP  MOVSB

 	LEA DX, M4
	XYZ 9

	LEA BX, CONC
	MOV CL, ACTUAL1
        	ADD CL, ACTUAL2
        	ADC CH, 0
BACK1:	MOV DL, [BX]
	XYZ 2
	INC BX
	LOOP BACK1
    
        RET
ENDP

XYZ 4CH

END
	

